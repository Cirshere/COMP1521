// swap pairs of bits of a 64-bit value, using bitwise operators

#include <assert.h>
#include <stdint.h>
#include <stdlib.h>

#define N_BITS 64

// return value with pairs of bits swapped
uint64_t bit_swap(uint64_t value) {
    
    uint64_t new = 0;
    
    new = (value << 1) | (value >> 1); 
        

    
    return new;
}
